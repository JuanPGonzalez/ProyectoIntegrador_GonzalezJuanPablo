package com.jpgportfolio.jpg.Security.Service;

import com.jpgportfolio.jpg.Security.Entity.Usuario;
import com.jpgportfolio.jpg.Security.Entity.UsuarioPrincipal;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.core.userdetails.UsernameNotFoundException;
import org.springframework.stereotype.Service;

/**
 *
 * @author Juan Pablo Gonzalez
 */

@Service
public class UsersDetailsImpl implements UserDetailsService {
    @Autowired
    UsuarioService usuarioService;

    @Override
    public UserDetails loadUserByUsername(String nombreUsuario) throws UsernameNotFoundException {
        Usuario usuario = usuarioService.getByNombreUsuario (nombreUsuario).get();
        return UsuarioPrincipal.build(usuario);
    }
}
